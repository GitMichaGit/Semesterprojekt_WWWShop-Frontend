function produktAnzeigen(nr) {
    if (nr === 1) {
        document.getElementById("IPHONE").style.display = "block";
        document.getElementById("SAMSUNG").style.display = "none";
    }
    if (nr === 2) {
        document.getElementById("IPHONE").style.display = "block";
        document.getElementById("SAMSUNG").style.display = "none";
    }
}